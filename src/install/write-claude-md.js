// Generates a brain-aware CLAUDE.md at the install root so that any local
// Claude Code SSH session running in this directory knows how to talk to
// Bumblebot's shared brain (recall + capture). Same pattern as Mandela's
// CLAUDE.md "Shared Brain" section.

const fs = require('node:fs');
const path = require('node:path');

const BRAIN_SECTION_MARKER = '## Shared Brain';

function redactDatabaseUrl(url) {
  if (!url) return '(not configured)';
  try {
    const u = new URL(url);
    if (u.password) u.password = '***';
    if (u.username) u.username = u.username || 'user';
    return u.toString();
  } catch {
    return url.replace(/:\/\/[^@]*@/, '://***:***@');
  }
}

function generateClaudeMd(config = {}) {
  const botName = config.botName || 'Bumblebot';
  const databaseUrl = redactDatabaseUrl(config.databaseUrl);
  const installRoot = config.installRoot || '.';

  return `# ${botName} — Local Claude Code Protocol

You are operating inside a **${botName}** install. ${botName} is a self-hosted
Telegram + Claude Code chassis with a Postgres-backed shared brain. This file
tells you how to use that brain from any local Claude Code session running in
this directory.

## Shared Brain

${botName} stores facts, decisions, and events in a Postgres database
(pgvector + BM25). The same brain is read and written by:

- The Telegram bot (\`src/telegram.js\`)
- The dispatched agent runner (\`src/agent-runner.js\`)
- Local Claude Code SSH sessions (you, right now)
- The dreaming loop (\`src/brain/dream.js\`)

Connection comes from \`${installRoot}/.env\` (\`DATABASE_URL\`). At install
time it was set to:

    ${databaseUrl}

### Recall — what do we already know?

Before asking the user "where is X?" or "have we discussed Y?", search the
brain first:

\`\`\`bash
${installRoot}/bin/bumblebot recall "your query here"
\`\`\`

This runs a hybrid pgvector + BM25 search across every captured fact, scoped
by agent namespace. Use it whenever you might be about to ask a locate-style
question — the answer is often already stored.

### Capture — write durable facts back

When you learn something non-obvious during a session (a decision the user
made, a fact about a person/project, a constraint that will outlive this
turn), capture it:

\`\`\`bash
${installRoot}/bin/bumblebot capture "Wayne's onboarding shipped via PR #268, chat-first approach"
\`\`\`

Rules of thumb:

- Capture **decisions and durable facts**, not status snapshots or cron output.
- Significance ≥ 6 for things you want the dreaming loop to keep and
  consolidate.
- Don't capture secrets — \`.env\` values, API keys, tokens never go in.

### When to use the brain

| Situation                                    | Action                                |
|----------------------------------------------|---------------------------------------|
| About to ask "where is…" or "did we…"        | \`bumblebot recall\` first            |
| User makes a decision worth remembering      | \`bumblebot capture\`                 |
| User mentions a person/project for first time| \`bumblebot capture\`                 |
| User asks "what did we decide about X?"      | \`bumblebot recall "X"\`              |
| Cron output, ephemeral state, transient logs | **don't** capture                     |

The shared brain is the single source of truth across Telegram sessions, SSH
sessions, and any \`${botName}\` agent. Treat it as your long-term memory.

## CLI Surface

The full \`bumblebot\` CLI lives at \`${installRoot}/bin/bumblebot\`:

| Command                        | Purpose                                    |
|--------------------------------|--------------------------------------------|
| \`bumblebot recall <query>\`   | Hybrid search across the brain.            |
| \`bumblebot capture <text>\`   | Write a fact to the brain.                 |
| \`bumblebot doctor\`           | Audit the install for security issues.     |
| \`bumblebot dream-now\`        | Run the dreaming/consolidation loop.       |
| \`bumblebot start\` / \`stop\` | Control the Telegram bot via pm2.          |

See \`${installRoot}/SPEC.md\` for the full specification.

## Security Notes

- \`.env\` is gitignored and a pre-commit hook blocks committing it. Don't
  override either.
- Never echo secrets into the brain or into Telegram replies.
- The Telegram allowlist (\`TELEGRAM_ALLOWLIST\`) is required — if it's empty,
  the bot refuses to start. This is intentional.

---

_This file was generated by \`${installRoot}/install\`. Re-running the install
script will regenerate it. Edits between brain section markers
(\`${BRAIN_SECTION_MARKER}\` … end of "Security Notes") may be overwritten._
`;
}

function writeClaudeMd(installRoot, config = {}) {
  if (!installRoot) throw new Error('installRoot is required');
  const target = path.join(installRoot, 'CLAUDE.md');
  const contents = generateClaudeMd({ ...config, installRoot });
  fs.writeFileSync(target, contents, { encoding: 'utf8', mode: 0o644 });
  return target;
}

function loadEnv(envPath) {
  const out = {};
  if (!fs.existsSync(envPath)) return out;
  const raw = fs.readFileSync(envPath, 'utf8');
  for (const line of raw.split('\n')) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith('#')) continue;
    const eq = trimmed.indexOf('=');
    if (eq === -1) continue;
    const key = trimmed.slice(0, eq).trim();
    let value = trimmed.slice(eq + 1).trim();
    if (
      (value.startsWith('"') && value.endsWith('"')) ||
      (value.startsWith("'") && value.endsWith("'"))
    ) {
      value = value.slice(1, -1);
    }
    out[key] = value;
  }
  return out;
}

module.exports = { generateClaudeMd, writeClaudeMd, redactDatabaseUrl, loadEnv };

if (require.main === module) {
  const installRoot = process.argv[2] || process.cwd();
  const env = loadEnv(path.join(installRoot, '.env'));
  const target = writeClaudeMd(installRoot, {
    botName: env.BOT_NAME || 'Bumblebot',
    databaseUrl: env.DATABASE_URL || '',
  });
  process.stdout.write(`wrote ${target}\n`);
}
